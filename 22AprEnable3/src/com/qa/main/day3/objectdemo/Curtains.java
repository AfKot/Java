package com.qa.main.day3.objectdemo;

public class Curtains {

	public boolean thermal;
	public float length;
	public float width;
	public int weights;
	public String colour;
	
	public Curtains(boolean thermalParam, float lengthParam, float widthParam, int weightsParam, String colourParam) {
		
		this.thermal = thermalParam;
		this.length = lengthParam;
		this.width = widthParam;
		this.weights = weightsParam;
		this.colour = colourParam;
		
	}
}
